const STATUS_COLORS = {
  yellow: 'yellow-500',
  green: 'green-500',
  red: 'red-500',
} as const

// Com esse "as const" você define que cada propriedade no objeto é uma constante, sendo assim, seus valores não podem ser alterados. Isso ocorre porque quando definimos STATUS_COLORS como constante estamos dizendo que ele sempre será um objeto e isso não poderá ser mudado, porém suas propriedades podem ser alteradas.

interface StatusProps {
  statusColor: keyof typeof STATUS_COLORS
}

// Utilizando "keyof typeof" nós falamos para a propriedade ter como valores as chaves das propriedades do objeto. Dessa forma, o valor poderá ser alterado dinamicamente caso nós adicionemos novas propriedades ao objeto STATUS_COLORS.

export const Status = styled.span<StatusProps>`
  display: flex;
  align-items: center;
  gap: 0.5rem;

  &::before {
    content: '';
    width: 0.5rem;
    height: 0.5rem;
    border-radius: 50%;
    background: ${(props) => props.theme[STATUS_COLORS[props.statusColor]]};
  }
`
